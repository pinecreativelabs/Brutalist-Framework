		<div class="info padded" style="margin-top: 1.5rem;"><h3 class="flow-text" style="margin: 0;"><strong>Text Shadow</strong></h3>
			<p>Simply apply <span class="bigger bold"><em>-t-s</em></span> (text-shadow) after a core color class to change the text shadow color.</p>
		</div>
		<div class="disabled padded" style="margin-bottom: 15px;">
			<p><strong>EXAMPLE:</strong></p>
			<p class="flow-text"><span class="green-t-s">.green-t-s</span></p>
		</div>
	</div><div class="half padded">
		<div class="info padded"><h3 class="flow-text" style="margin: 0;"><strong>Border &amp; Outline Color</strong></h3>
			<p>Simply apply <strong><em>-b</em></strong> (border) or <strong><em>-o</em></strong> (outline) after a core color class to change the border or outline color.<br />
			<strong>See "Basic Borders" and "Outlines" under BUTCH on how to apply borders.</strong></p>
		</div>
		<div class="disabled padded" style="margin-bottom: 15px;">
			<p><strong>EXAMPLE:</strong></p>
			<p class="o-d-f plum-o green-b b-a-k" style="margin-top: 1.8em;">Thick dashed green border with fat dotted plum outline<br />
				<span class="bold">.plum-o .green-b</span></p>
		</div>
		<div class="info padded" style="margin-top: 1.5rem;"><h3 class="flow-text" style="margin: 0;"><strong>Change Background Color on Hover</strong></h3>
			<p>The background color can be changed upon hover by applying a <em><strong>-h</strong></em> after the class name. In the example below, we change a banana box to a mango upon hover:</p>
		</div>
		<div class="disabled padded" style="margin-bottom: 15px;">
			<p class="inline-block banana mango-h arial chocolate-t"><span class="flow-text">HOVER ME</span><br /><strong>.banana.mango-h</strong></p>
		</div>
