<h3 class="fluid-text monolisk" id="overview">Overview</h3>
<p class="bitstream">The CORE includes <a href="https://www.blueprintgrid.com" target="_blank" class="lemonlink">Blueprint Grid</a>, a multi-method layout library built with Flexbox and CSS Grid. You'll only need to include the following CSS file:</p>
<p class="requiresbox bitstream"><span class="flow-text bold"><em>bos/core/css/blueprintgrid.css</em></span><br /><span class="echo">$blueprintgrid_css</span></p>
<p class="bitstream" style="margin-top: 1.5rem;">This imports all necessary CSS components from the following directory:<br />
<strong><span class="terminal">bos/core/css/grids/</span></strong></p>
<p class="bitstream" style="margin-top: 1.5rem;">There are six layout methods included:</p>
	<ul class="square bitstream col2">
		<li>B3Grid</li>
		<li>CSS Grid</li>
		<li>Print Grid</li>
		<li>Bento Grid</li>
		<li>Chaos Grid</li>
		<li>PolyGrids</li>
	</ul>