	<h3 class="monolisk fluid-text-h2 red-t center" style="text-shadow: 4px 3px #fff;"><strong>A Dualistic APProach</strong></h3>
</div>
<div class="flex bitstream"><div class="padded full white black-t black-b b-s-k">
	<p><em>Brutalist Framework</em> was built with an <strong>application-first</strong> design approach. As the web experience has become primarily mobile, it makes sense to design websites to look and feel more like a mobile application. The design approach process is dualistic: it is <em>both</em> deconstructive and constructive.</p>
</div><div class="padded half mono">
	<h4 class="monolisk fluid-text"><strong>Deconstruct</strong></h4>
	<p class="flow-text red-t">&#9626;\&#9697;\&#9698;\&#9709;\&#9556;\&#9706;\&#9703;\&#9693;\&#9623;</p>
	<p>Break down overall concepts and ideas into dislocated, raw elements and components.</p>
	<ul class="check">
		<li>Analyze project needs &amp; concepts</li>
		<li>Break down into basic components</li>
		<li>Compartmentalize content</li>
	</ul>
	<p>The idea is to challenge the current or conventional design (if one exists), and explore the possibilities of what it could become.</p>
</div><div class="padded half mono-inv">
	<h4 class="monolisk fluid-text"><strong>Construct</strong></h4>
		class="flow-text lemon-t">&#9623;&#9626;&#9556;&#9693;&#9697;&#9698;&#9706;__&#9709;__&#9703;</p>
	<p>Assemble, consolidate, and build components into a creative end product that we're happy with.</p>
	<ul class="check">
		<li>Design &amp; construct prototype(s)</li>
		<li>Implement content</li>
		<li>Publish changes</li>
	</ul>
	<p>The goal is to actively build a solution that progresses a concept to evolve based on pre-esisting data or experiences.</p>
</div></div>
