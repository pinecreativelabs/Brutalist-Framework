<h3 class="fluid-text monolisk" id="fancy">Fancy Filters</h3>
<p class="bitstream" style="margin: 0 0 1rem 0;">Fancy filters combine multiple filter properties to create a preset filter effect. Filtered elements need to be wrapped within a filter container. An example markup:</p>
<p class="terminal inline-block"><strong>&lt;div class="filter <em>filter-name</em>"&gt; &lt;any-elements&gt; &lt;/div&gt;</strong></p>
<p class="bitstream" style="margin: 1rem 0 1rem 0;">All direct child elements within a fancy <em>filter class</em> container will be affected. The class <strong><em>filter</em></strong> must be applied in conjunction with a filter class (ex: <strong><em>.filter.firestorm</em></strong>).</p>
